  String tr(String key, {Map<String, String> args = const {}}) {
    final map = isArabic ? _ar : _en;
    var value = map[key] ?? _en[key] ?? key;
    for (final e in args.entries) {
      value = value.replaceAll('{${e.key}}', e.value);
    }
    return value;
  }

  String categoryLabel(String category) {
    final key = 'category.$category';
    final map = isArabic ? _ar : _en;
    if (map.containsKey(key) || _en.containsKey(key)) return tr(key);
    return category;
  }

